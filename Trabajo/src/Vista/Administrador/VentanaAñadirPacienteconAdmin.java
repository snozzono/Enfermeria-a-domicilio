    private void brnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_brnCrearActionPerformed
        int rut;
        String usuario, contraseña;
        rut = Integer.parseInt(txtRut.getText());
        usuario = txtUsuario.getText();
        contraseña = txtContraseña.getText();
        int run_tec_run = manejarSeleccionTecnico();

        Paciente tec = new Paciente(rut, run_tec_run, usuario, contraseña);
        TecnicoDAO tecDAO = new TecnicoDAO();

        if (txtRut.getText().length() > 0 && txtUsuario.getText().length() > 0 && txtContraseña.getText().length() > 0) {
            if (pacienteDAO.ValidarPac(rut, run_tec_run) == null) {
                pacienteDAO.ingresarPaciente(tec);
                JOptionPane.showMessageDialog(this, usuario + " añadido correctamente");

            } else {
                JOptionPane.showMessageDialog(rootPane, "Ese paciente ya está registrado");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Ingresa datos validos porfavor");
        }
    }//GEN-LAST:event_brnCrearActionPerformed

