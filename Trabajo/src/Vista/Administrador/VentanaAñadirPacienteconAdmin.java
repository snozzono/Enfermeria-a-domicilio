    public void llenarJcbTecnicos() {

        jcbTecnicos.setEnabled(true);
        jcbTecnicos.removeAllItems();
        ArrayList<Tecnico> lista = tecnicoDAO.obtenerTodos();
        for (Tecnico tmp : lista) {
            String items = tmp.getRun_tec() + " - " + tmp.getUsuario();
            jcbTecnicos.addItem(String.valueOf(items));
        }
    }

    public int manejarSeleccionTecnico() {
        String seleccionado = (String) jcbTecnicos.getSelectedItem();
        if (seleccionado != null && seleccionado.contains("-")) {
            String[] partes = seleccionado.split(" - ");
            int numero = Integer.parseInt(partes[0].trim()); 
            return numero;
        } else {
            return -1;
        }
    }


