            int buffer = bdDAO.buscar().getPaciente();
            Paciente tec = tecnicoDAO.buscarPac(buffer);
            tec.setDiagn(cambioDiagnostico);
            tecnicoDAO.modificarPaciente(tec, buffer);

            bd.setPaciente(buffer);
