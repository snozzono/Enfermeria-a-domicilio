    public void ActualizarRun() {

        int run = bdDAO.buscar().getPaciente();
        int texto = tecnicoDAO.buscarPac(run).getRun_pac();
        jRun.setText("Run actual: " + texto);
    }

    public void ActualizarUser() {

        int user = bdDAO.buscar().getPaciente();
        String text = tecnicoDAO.buscarPac(user).getNombre_p();
        jUser.setText("Nombre actual: " + text);

    }

    public void ActualizarDiagnostico() {

        int contra = bdDAO.buscar().getPaciente();
        String text = tecnicoDAO.buscarPac(contra).getDiagn();
        txtDiagnostico.setText("Diagnostico actual: " + text);

    }
