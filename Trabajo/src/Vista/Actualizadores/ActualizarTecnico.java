    public void ActualizarRun() {

        int ee = bdDAO.buscar().getTecnico();
        int run = tecnicoDAO.BuscarTecnicoPorRut(ee).getRun_tec();
        jRun.setText("Run actual: " + run);
    }

    public void ActualizarUser() {

        int tecnico = bdDAO.buscar().getTecnico();
        String user = tecnicoDAO.BuscarTecnicoPorRut(tecnico).getUsuario();
        jUser.setText("Nombre actual: " + user);

    }

    public void ActualizarConstraseña() {

        int tecnico = bdDAO.buscar().getTecnico();
        String contraseña = tecnicoDAO.BuscarTecnicoPorRut(tecnico).getPasswrd();
        jlContraseña.setText("Contraseña actual: " + contraseña);

    }
