    private void btnContraseñaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContraseñaActionPerformed

        String cambio = JOptionPane.showInputDialog("¿Que contraseña le vas a poner?").trim();
        String user = tecnicoDAO.BuscarTecnicoPorRut(bdDAO.buscar().getTecnico()).getUsuario();

        if (tecnicoDAO.ValidarTec(user, cambio) == null) {

            int buffer = bdDAO.buscar().getTecnico();
            Tecnico tec = tecnicoDAO.BuscarTecnicoPorRut(buffer);
            tec.setPasswrd(cambio);
            tecnicoDAO.modificarTecnicoConRun(tec, buffer);

            bd.setTecnico(buffer);
            bdDAO.modificarTecnico(bd);

            JOptionPane.showMessageDialog(rootPane, "Contraseña cambiada");
            ActualizarConstraseña();
    }                                          
        else {
            JOptionPane.showMessageDialog(rootPane, "Ya hay un user con esas credenciales");
        }

    }//GEN-LAST:event_btnContraseñaActionPerformed

    private void btnRunActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRunActionPerformed

        int cambio = Integer.parseInt(JOptionPane.showInputDialog("¿Que rut le vas a poner?").trim());

        if (tecnicoDAO.BuscarTecnicoPorRut(cambio) == null) {

            int buffer = bdDAO.buscar().getTecnico();

            bd.setTecnico(cambio);
            bdDAO.modificarTecnico(bd);

            Tecnico tec = tecnicoDAO.BuscarTecnicoPorRut(buffer);
            tec.setRun_tec(cambio);

            tecnicoDAO.modificarTecnicoConRun(tec, buffer);

            JOptionPane.showMessageDialog(rootPane, "Run cambiado");
            ActualizarRun();

        } else {
            JOptionPane.showMessageDialog(rootPane, "Ese Tecnico ya está registrado");
        }
    }//GEN-LAST:event_btnRunActionPerformed

    private void btnUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUsuarioActionPerformed

        String cambio = JOptionPane.showInputDialog("¿Que nombre le vas a poner?").trim();
        String contraseña = tecnicoDAO.BuscarTecnicoPorRut(bdDAO.buscar().getTecnico()).getPasswrd();

        if (tecnicoDAO.ValidarTec(cambio, contraseña) == null) {

            int buffer = bdDAO.buscar().getTecnico();
            Tecnico tec = tecnicoDAO.BuscarTecnicoPorRut(buffer);
            tec.setUsuario(cambio);
            tecnicoDAO.modificarTecnicoConRun(tec, buffer);

            bd.setTecnico(buffer);
            bdDAO.modificarTecnico(bd);

            JOptionPane.showMessageDialog(rootPane, "Usuario cambiado");
            ActualizarUser();
    }//GEN-LAST:event_btnUsuarioActionPerformed
        else {
            JOptionPane.showMessageDialog(rootPane, "Ese Tecnico ya existe");
        }
    }
